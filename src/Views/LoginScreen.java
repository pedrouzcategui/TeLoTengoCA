    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        EmployeeRepository employeeRepo = new EmployeeRepository();
        Optional<Employee> employee = employeeRepo.findByUsername(usernameField.getText());
        if(employee.isPresent()){
            // Check for password
            if(employee.get().checkPassword(passwordField.getText())){
                dispose();
                AppContext.getInstance().setCurrentEmployee(employee.get());
                LogRepository logRepo = new LogRepository();
                logRepo.addToCsv(new Log(new Date(), 
                        AppContext.getInstance().getCurrentEmployee().getId(), 
                        AppContext.getInstance().getCurrentEmployee().getName(),
                        "Nuevo inicio de sesión"
                ));
                if(employee.get().getRole() == 'A'){
                    new AdminScreen();
                } else {
                    new VendorScreen();
                }
                return;
            } else {
                JOptionPane.showMessageDialog(null, "Contraseña Incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        JOptionPane.showMessageDialog(null, "Usuario no existe", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }//GEN-LAST:event_loginButtonActionPerformed

